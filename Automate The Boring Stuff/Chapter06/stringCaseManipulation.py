my_name = 'Dallas'
print('Current value: ' + my_name)
# case manipulation
print('Convert to lowercase: ' + my_name.lower())
print('Convert to UPPERCASE: ' + my_name.upper())
print('Swap case: ' + my_name.swapcase())

# check for lowercase and uppercase and all alphas
print('Is the value of my_name in all lowercase? ' + str(my_name.islower()))
print('Is the value of my_name in all UPPERCASE? ' + str(my_name.isupper()))
print('Is the value of my_name all alpha characters? ' + str(my_name.isalpha()))
