# first argument is the start value (min)
# second argument is the value to stop at, but that value is excluded (max - 1)
for i in range(8, 16):
	print(i)
print('\n')

# first argument is the start value (min)
# second argument is the value to stop at, but that value is excluded (max - 1)
# third arugment is how much to increment by
for i in range(8, 16, 2):
	print(i)
print('\n')

# count down, start at 10, stop at 0, go down by 1
for i in range(10, -1, -1):
	print(str(i))
print('\n')
